#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    /*
    ceil floor sqrt pow abs
    */
    printf("%f\n",ceil(4.3));     // ==> Sayýyý üste yuvarlama
    printf("%f\n",floor(4.9));    // ==> Sayýyý tabana yuvarlama
    printf("%f\n",sqrt(67));      // ==> Sayýnýn karekökünü alma
    printf("%f\n",pow(3,3));      // ==> Üslü alam
    printf("%d\n",abs(15));       // ==> Bir sayýnýn sýfýra olan uzaklýðý yani mutlak deðer alma

    return 0;
}
